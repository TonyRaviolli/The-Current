// Pre-bake U.S. state SVG paths from authoritative TopoJSON.
// Runs once; output (assets/us-states-svg.js) is shipped to the client with
// zero runtime projection / topojson deps.
//
// Source: us-atlas states-10m.json (Albers USA projection, already projected
// to [960×600] by d3-geo convention).
//
// Usage: node scripts/build-us-states-svg.mjs

import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import { feature } from 'topojson-client';
import { geoPath, geoAlbersUsa } from 'd3-geo';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

const FIPS_TO_ABBR = {
  '01':'AL','02':'AK','04':'AZ','05':'AR','06':'CA','08':'CO','09':'CT','10':'DE','11':'DC',
  '12':'FL','13':'GA','15':'HI','16':'ID','17':'IL','18':'IN','19':'IA','20':'KS','21':'KY',
  '22':'LA','23':'ME','24':'MD','25':'MA','26':'MI','27':'MN','28':'MS','29':'MO','30':'MT',
  '31':'NE','32':'NV','33':'NH','34':'NJ','35':'NM','36':'NY','37':'NC','38':'ND','39':'OH',
  '40':'OK','41':'OR','42':'PA','44':'RI','45':'SC','46':'TN','47':'TX','48':'UT','49':'VT',
  '50':'VA','51':'WA','53':'WV','54':'WI','55':'WY','56':'PR'
};
// us-atlas uses state FIPS codes; PR (72) is not in the default us-atlas states.
// The FIPS_TO_ABBR above is incorrect for some codes. Fix it:
const CORRECT_FIPS_TO_ABBR = {
  '01':'AL','02':'AK','04':'AZ','05':'AR','06':'CA','08':'CO','09':'CT','10':'DE','11':'DC',
  '12':'FL','13':'GA','15':'HI','16':'ID','17':'IL','18':'IN','19':'IA','20':'KS','21':'KY',
  '22':'LA','23':'ME','24':'MD','25':'MA','26':'MI','27':'MN','28':'MS','29':'MO','30':'MT',
  '31':'NE','32':'NV','33':'NH','34':'NJ','35':'NM','36':'NY','37':'NC','38':'ND','39':'OH',
  '40':'OK','41':'OR','42':'PA','44':'RI','45':'SC','46':'SD','47':'TN','48':'TX','49':'UT',
  '50':'VT','51':'VA','53':'WA','54':'WV','55':'WI','56':'WY'
};

async function main() {
  const topo = JSON.parse(
    await readFile(path.join(ROOT, 'node_modules/us-atlas/states-10m.json'), 'utf8')
  );

  // us-atlas/states-10m.json is WGS84 (lon/lat). Project with Albers USA
  // (handles AK/HI insets automatically). Fit into a 960×600 canvas.
  const VB_W = 960;
  const VB_H = 600;
  const fc = feature(topo, topo.objects.states);

  const projection = geoAlbersUsa().fitSize([VB_W, VB_H], fc);
  const path2d = geoPath(projection);

  const vbX = 0;
  const vbY = 0;
  const vbW = VB_W;
  const vbH = VB_H;

  const paths = [];
  for (const f of fc.features) {
    const fips = String(f.id).padStart(2, '0');
    const abbr = CORRECT_FIPS_TO_ABBR[fips];
    if (!abbr) continue;
    const name = f.properties?.name || abbr;
    const d = path2d(f);
    if (!d) continue;
    paths.push({ id: abbr, fips, name, d });
  }
  paths.sort((a, b) => a.name.localeCompare(b.name));

  const header = `// AUTO-GENERATED by scripts/build-us-states-svg.mjs — do not edit by hand.
// Source: us-atlas/states-10m.json (Albers USA projection, pre-projected).
// ViewBox is authoritative; hit paths are the exact rendered state borders,
// so hover overlays cannot drift from the visible boundaries.

export const US_STATES_SVG_VIEWBOX = { x: ${vbX}, y: ${vbY}, w: ${vbW}, h: ${vbH} };

export const US_STATES_SVG_PATHS = ${JSON.stringify(paths, null, 2)};
`;

  const outPath = path.join(ROOT, 'assets/us-states-svg.js');
  await writeFile(outPath, header, 'utf8');
  console.log(`Wrote ${paths.length} state paths to ${outPath}`);
  console.log(`ViewBox: ${vbX} ${vbY} ${vbW} ${vbH}`);
}

main().catch((e) => { console.error(e); process.exit(1); });
